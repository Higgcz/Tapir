#import "TSLEntity.h"
#import "TSLBody.h"

@implementation TSLEntity

#pragma mark - Updating

////////////////////////////////////////////////////////////////////////////////////////////////
- (instancetype) initWithBodySize:(CGSize) size
////////////////////////////////////////////////////////////////////////////////////////////////
{
    self = [super init];
    if (self) {
        self.body = [TSLBody bodyWithSize:size];
    }
    return self;
}

////////////////////////////////////////////////////////////////////////////////////////////////
- (void) updateWithTimeSinceLastUpdate:(CFTimeInterval) deltaTime
////////////////////////////////////////////////////////////////////////////////////////////////
{
    [self.body resetChanges];
}

#pragma mark - Callbacks

////////////////////////////////////////////////////////////////////////////////////////////////
- (void) didCreatedAtUniverse:(TSLUniverse *) universe
////////////////////////////////////////////////////////////////////////////////////////////////
{
    _universe = universe;
}

////////////////////////////////////////////////////////////////////////////////////////////////
- (void) didDeleted
////////////////////////////////////////////////////////////////////////////////////////////////
{
    _universe = nil;
}

@end
